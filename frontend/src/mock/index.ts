export function generateMockPaper(title: string, duration: number, questionCounts: Record<string, number>): ExamPaper {
  let totalQuestions = 0;
  let totalScore = 0;
  
  Object.values(questionCounts).forEach(count => {
    totalQuestions += count;
    if (count > 0) totalScore += count * 2;
  });
  
  return {
    id: Date.now(),
    title,
    description: '智能生成的试卷',
    totalScore,
    duration,
    passScore: Math.round(totalScore * 0.6),
    questionCount: totalQuestions,
    status: 0,
    createTime: new Date().toLocaleString(),
  };
}

